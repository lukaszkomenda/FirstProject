package CwiczeniaPraktyczneJavaMarcinLis;

public class dzialaniaModulo {
    public static void main(String[] args) {
        int a = 10;
        int b = 25;
        int c = b % a;

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);

        c = a * b;
        System.out.println(c);
    }
}
